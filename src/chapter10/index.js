(function(){
    document.getElementById("output").innerHTML = "Hello Chapter 10"
})();
// The above function will simply check the HTML element whose 'id' equals 'output' and set the inner HTML content to "Hello Chapter 10". Enclosing the whole function in a bracket — i.e. (function() ) — and adding another bracket at the end of function declaration causes the function to be auto-executed without the need to be called first. Such functions are conventionally referred to as immediately invoked function expression (IIFE), pronounced iffy.

